    public boolean isEmptyStudent() {
        //Checking if the user filled the student's name field
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's name is empty.");
            return false;
        }
        //Checking if the date of birth is selected
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Student's date of birth is not selected.");
            return false;
        }

        //Checking for the validity of the selected birthdate
        /**
         * if(jDateChooser1.getDate().compareTo(new Date())>0){
         * JOptionPane.showMessageDialog(this, "Student's date of birth is not
         * selected."); return false; }
         */
        //Checking if the user filled the student's email field
        if (jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student's email is empty.");
            return false;
        }
        //Checking for the validity of filled student's email field
        if (!jTextField3.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address.");
            return false;
        }
        //Checking for the phone number lenght
        if (jTextField4.getText().length() > 15) {
            JOptionPane.showMessageDialog(this, "Phone number is too long.");
            return false;
        }
        //Checking for the phone number
        if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Empty student's phone number.");
            return false;
        }
        //Checking for the father's name
        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student father's name is empty.");
            return false;
        }
        //Checking for the mother's name
        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student mother's name.");
            return false;
        }
        //Checking for the nationality
        if (jTextField9.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Empty student's nationality.");
            return false;
        }
        //Checking for the address
        if (jTextField8.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Empty student's address.");
            return false;
        }
        //Checking if the image is selected
        if (imagePath == null) {
            JOptionPane.showMessageDialog(this, "Please add your image");
            return false;
        }
        return true;
    }
